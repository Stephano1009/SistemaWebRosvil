
package Entidades;


public class Proveedor {
    private int codigoProve;
    private String rucProvee;
    private String nombreProve;
    private String telefonoProve;
    private boolean estadoProve;

    public int getCodigoProve() {
        return codigoProve;
    }

    public void setCodigoProve(int codigoProve) {
        this.codigoProve = codigoProve;
    }

    public String getRucProvee() {
        return rucProvee;
    }

    public void setRucProvee(String rucProvee) {
        this.rucProvee = rucProvee;
    }

    public String getNombreProve() {
        return nombreProve;
    }

    public void setNombreProve(String nombreProve) {
        this.nombreProve = nombreProve;
    }

    public String getTelefonoProve() {
        return telefonoProve;
    }

    public void setTelefonoProve(String telefonoProve) {
        this.telefonoProve = telefonoProve;
    }

    public boolean isEstadoProve() {
        return estadoProve;
    }

    public void setEstadoProve(boolean estadoProve) {
        this.estadoProve = estadoProve;
    }

    
    

    
}
